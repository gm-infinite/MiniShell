#include "../main/minishell.h"

char	**split_to_args(t_split split)
{
	char	**args;

	args = allocate_args_array(split.size);
	if (!args)
		return (NULL);
	if (copy_split_strings(args, split) == -1)
		return (NULL);
	return (args);
}

void	free_args(char **args)
{
	int	i;

	if (!args)
		return ;
	i = 0;
	while (args[i])
	{
		free(args[i]);
		i++;
	}
	free(args);
}
